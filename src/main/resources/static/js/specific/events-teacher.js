$(document).ready(init);

function init(){
    fillGroupTable();

    $("#plannedEvents").one("click", fillEventsTable);
}

function fillGroupTable() {
    var titles = ['id', 'Номер', 'Спеціальність', 'Курс', 'Тип навчання', 'Ступінь'];
    insertTable(titles, "content-table-groups");
    var entityParams = ['id', 'number', 'speciality', 'courseNumber', 'groupType', 'groupDegree'];
    fillTableFromWithLinks("content-table-groups", API + "/groups/getAllWithoutCurators", entityParams, "number");
}

function fillEventsTable() {
    var titles = ['id', 'Тип', 'Дата початку', 'Дата завершення'];
    insertTable(titles, "content-table-events");
    var entityParams = ['id', 'eventType', 'beginDate', 'expirationDate'];
    fillTableFromWithLinks("content-table-events", API + "/events/getAll", entityParams, "eventType");
}
